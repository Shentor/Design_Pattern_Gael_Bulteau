<h1>Command</h1>
<p>Le Design Pattern Command permet de créer un intemédiaire entre les Modèles et la vue. 
La commande encapsule la requête de l'invoqueur et utilise les informations contenues dans 
les modèles et les rend disponibles a la vue. Cela permet d'alléger les modèles et de faciliter 
leur utilisation dans la vue.</p>
